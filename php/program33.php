<?php
// Creating a 3x3 matrix (multidimensional array)
$matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];

// Accessing the value in the second row and third column
$value = $matrix[1][2]; // (row index 1 → second row, column index 2 → third column)

echo "The value in the second row and third column is: " . $value;
?>


The value in the second row and third column is: 6
